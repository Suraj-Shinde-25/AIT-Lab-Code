function translate() {
    var element = document.getElementById("myElement");
    element.style.transform = "translate(50px, 50px)";
}

function rotate() {
    var element = document.getElementById("myElement");
    element.style.transform = "rotate(45deg)";
}

function scale() {
    var element = document.getElementById("myElement");
    element.style.transform = "scale(1.5)";
}
